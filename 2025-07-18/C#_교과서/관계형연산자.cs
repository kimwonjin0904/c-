using System;
using System.Text;

public class 관계형연산자
{
    public static void Main()
    {
        int x = 3;
        int y = 5;
        Console.WriteLine(x == y);//false

        Console.WriteLine(x != y);//true !는 아닐떄

        Console.WriteLine(x > y);//false x가 y보다 클때 출력값:false

        Console.WriteLine(x <= y);//true x가 y보다 작거나 같을때 출력값:true
        


    }

}
