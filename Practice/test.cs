using System;
using System.Diagnostics.CodeAnalysis;
using static System.Console;

public class 가로구구단
{
    public static void Main()
    {
        int count = 0;   //초기식
        do
        {
            Console.WriteLine("안녕하세요"); //실행문
            count++; //증감식
        }

        while (count < 3);
    }
}


